    void auto_idtransaksi() {
        try {
            Connection con = Koneksi.getConnection();
            Statement stat = con.createStatement();
            String sql = "SELECT MAX(right(id_transaksi,1)) AS no FROM transaksi";
            ResultSet rs = stat.executeQuery(sql);
            while (rs.next()) {
                if (rs.first() == false) {
                    txt_id_transaksi.setText("TR00");
                } else {
                    rs.last();
                    int set_id = rs.getInt(1) + 1;
                    String no = String.valueOf(set_id);
                    int id_next = no.length();
                    for (int n = 0; n < 3 - id_next; n++) {
                        no = "0" + no;
                    }
                    txt_id_transaksi.setText("TR" + no);
                }

            }
        } catch (Exception e) {
        }
    }
