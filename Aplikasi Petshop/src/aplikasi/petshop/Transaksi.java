    private void tampil_jumlah() {
        try {
            int total_grooming = 0;
            String idd = "";
            int total_transaksi = 0;
            int total_penitipan = 0;

            idd = label_id_transaksi.getText().toString();
            stt = cn.createStatement();
            rs = stt.executeQuery("select * from data_grooming  where id_transaksi='" + idd + "'");
            if (idd == null) {
                total_grooming = 0;
            } else {
                while (rs.next()) {
                    total_grooming = 0 + Integer.parseInt(rs.getString("sub_total"));
                }
                stt = cn.createStatement();
                rs = stt.executeQuery("select * from room  where id_transaksi='" + idd + "'");
                while (rs.next()) {
                    total_penitipan = total_penitipan + Integer.parseInt(rs.getString("sub_total"));
                    total_transaksi = total_grooming + total_penitipan;
                    label_totall.setText(String.valueOf(total_transaksi));

                }

            }
        } catch (Exception e) {

        }
    }
