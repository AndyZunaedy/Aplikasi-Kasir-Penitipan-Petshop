    private void auto_number_hewan() {
        try {
            Connection con = Koneksi.getConnection();
            Statement stat = con.createStatement();
            String sql = "SELECT MAX(right(id_hewan,2)) AS no FROM data_hewan";
            ResultSet rs = stat.executeQuery(sql);
            while (rs.next()) {
                if (rs.first() == false) {
                    txt_id_hewan.setText("H001");

                    fld_id_hewan_pelanggan.setText("H001");
                } else {
                    rs.last();
                    int set_id = rs.getInt(1) + 1;
                    String no = String.valueOf(set_id);
                    int id_next = no.length();
                    for (int n = 0; n < 3 - id_next; n++) {
                        no = "0" + no;
                    }
                    txt_id_hewan.setText("H" + no);

                    fld_id_hewan_pelanggan.setText("H" + no);

                }

            }
        } catch (Exception e) {
        }
    }

