    private void tampil_penitpan() {
        model = new DefaultTableModel();
        int row = model.getRowCount();
        for (int i = 0; i < row; i++) {
            model.removeRow(0);
        }
        try {
            Connection cn = Koneksi.getConnection();
            ResultSet rs = cn.createStatement().executeQuery(" SELECT * FROM room WHERE jenis_layanan LIKE 'penitipan%'");
            while (rs.next()) {
                String[] data = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7), rs.getString(8), rs.getString(9), rs.getString(10), rs.getString(11)};
                model.addRow(data);
            }

        } catch (Exception e) {

        }
        int jumlah = model.getRowCount();
        txt_jumlahpenitipan.setText("" + jumlah);
    }

    private void tampil_tabel_kandang() {
        model = new DefaultTableModel();
        int row = model.getRowCount();
        for (int i = 0; i < row; i++) {
            model.removeRow(0);
        }
        try {
            Connection cn = Koneksi.getConnection();
            ResultSet rs = cn.createStatement().executeQuery(" SELECT * FROM kandang WHERE status_kandang LIKE 'Tersedia%'");
            while (rs.next()) {
                String[] data = {rs.getString(1), rs.getString(2), rs.getString(3)};
                model.addRow(data);
            }

        } catch (Exception e) {

        }
        int jumlah = model.getRowCount();
        txt_jumlah_kandang.setText("" + jumlah);
    }//room penitipan

