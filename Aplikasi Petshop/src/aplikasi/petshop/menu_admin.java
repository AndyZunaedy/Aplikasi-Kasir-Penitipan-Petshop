    private void tampil_tabel_paket() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        Connection con = Koneksi.getConnection();
        try {
            stt = con.createStatement();
            String sql = " SELECT * FROM data_paket WHERE kode_paket LIKE 'GR%'";
            rss = stt.executeQuery(sql);
            while (rss.next()) {
                Object[] o;
                o = new Object[4];
                o[0] = rss.getString(1);
                o[1] = rss.getString(2);
                o[2] = rss.getString(3);
                o[3] = rss.getString(4);

                model.addRow(o);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void auto_number_paket_grooming() {
        try {
            Connection con = Koneksi.getConnection();
            Statement stat = con.createStatement();
            String sql = "SELECT MAX(right(kode_paket,1)) AS no FROM data_paket";
            ResultSet rs = stat.executeQuery(sql);
            while (rs.next()) {
                if (rs.first() == false) {
                    txtidpaketgrooming.setText("GR00");
                } else {
                    rs.last();
                    int set_id = rs.getInt(1) + 1;
                    String no = String.valueOf(set_id);
                    int id_next = no.length();
                    for (int n = 0; n < 3 - id_next; n++) {
                        no = "0" + no;
                    }
                    txtidpaketgrooming.setText("GR" + no);
                }

            }
        } catch (Exception e) {
        }
    }

