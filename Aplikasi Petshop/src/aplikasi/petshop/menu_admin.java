    private void auto_number_kandang() {
        try {
            Connection con = Koneksi.getConnection();
            Statement stat = con.createStatement();
            String sql = "SELECT MAX(right(nomor_kandang,1)) AS no FROM kandang";
            ResultSet rs = stat.executeQuery(sql);
            while (rs.next()) {
                if (rs.first() == false) {
                    txt_no_kandang.setText("KD0");
                } else {
                    rs.last();
                    int set_id = rs.getInt(1) + 1;
                    String no = String.valueOf(set_id);
                    int id_next = no.length();
                    for (int n = 0; n < 3 - id_next; n++) {
                        no = "0" + no;
                    }
                    txt_no_kandang.setText("KD" + no);
                }

            }
        } catch (Exception e) {
        }
    }

